function applyLanguage(lang) {
  // 語言切換時調整 body class，動態套用字體
  document.body.classList.remove('font-zh', 'font-en');
  if (lang === 'zh') {
    document.body.classList.add('font-zh');
  } else {
    document.body.classList.add('font-en');
  }

  // 原本的語言內容切換程式碼
  document.querySelectorAll('[data-lang]').forEach(function(el) {
    const key = el.getAttribute('data-lang');
    if (translations[lang][key]) {
      if (
        /(_desc|_content|_html)$/.test(key)
      ) {
        el.innerHTML = translations[lang][key];
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });
}

function toggleLanguage() {
  const currentLang = localStorage.getItem('lang') || 'zh';
  const newLang = currentLang === 'zh' ? 'en' : 'zh';
  localStorage.setItem('lang', newLang);
  applyLanguage(newLang);
}

// 頁面載入時自動載入語言
document.addEventListener("DOMContentLoaded", function () {
  const savedLang = localStorage.getItem("lang") || "zh";
  applyLanguage(savedLang);
});
function switchLanguage() {
  const selectedLang = document.getElementById("langSelector").value;
  localStorage.setItem("selectedLanguage", selectedLang);
  applyLanguage(selectedLang);
}

window.onload = () => {
  const savedLang = localStorage.getItem("selectedLanguage") || "zh";
  applyLanguage(savedLang);
  document.body.classList.add("visible");
};
